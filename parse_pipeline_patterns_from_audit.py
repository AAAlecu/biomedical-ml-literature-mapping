#!/usr/bin/env python3
"""
Pipeline Architecture Pattern Parsing Script
--------------------------------------------

Purpose
-------
Parses previously audited OpenAlex paper corpora in order to identify
high-level machine learning pipeline and architectural design patterns
across the four prediction regimes:

    S = structured multiscale prediction
    H = high-dimensional biomedical signals
    M = multimodal learning
    T = temporal and longitudinal modeling

Unlike the original OpenAlex retrieval pipeline, this script DOES NOT
query OpenAlex again. Instead, it operates on already curated and
audited paper collections generated by the earlier parsing stages.

The script:
1) loads regime-specific paper audit logs
2) identifies accessible/analyzable papers using audit inclusion flags
3) recovers metadata and abstracts from the original OpenAlex CSV exports
4) optionally retrieves DOI landing pages and PDF full text
5) extracts large-scale architectural and pipeline-design patterns
6) builds prevalence statistics using accessible papers as denominators
7) exports:
      - prevalence tables
      - raw count tables
      - audit logs with evidence snippets
      - dataset-level coverage summaries
      - LaTeX tables
      - JSON parsing reports

Pipeline architecture categories detected
-----------------------------------------
The script detects five major architectural/pipeline pattern classes:

1. Handcrafted feature pipelines
2. Learned representation pipelines
3. Fusion-centric architectures
4. Sequential predictive pipelines
5. Robust / transfer-aware pipelines

Default data flow
-----------------
Audit log input:
    regime_parse_outputs/<REGIME>/<REGIME>_paper_audit_log.csv

Optional metadata recovery:
    query_outputs/<REGIME>/openalex_*_<REGIME>_ml_top350.csv

"""

