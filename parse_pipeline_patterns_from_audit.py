def counts_to_tables(
    pattern_counts: Dict[str, Counter],
    denominators: Dict[str, int],
    add_total: bool = True,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    datasets = sorted(denominators.keys())
    counts_df = pd.DataFrame(index=PATTERN_ORDER, columns=datasets, dtype=int).fillna(0)
    prev_df = pd.DataFrame(index=PATTERN_ORDER, columns=datasets, dtype=float).fillna(0.0)
    for pattern in PATTERN_ORDER:
        for ds in datasets:
            count = int(pattern_counts.get(ds, Counter()).get(pattern, 0))
            n = int(denominators.get(ds, 0))
            counts_df.loc[pattern, ds] = count
            prev_df.loc[pattern, ds] = round(100.0 * count / n, 1) if n else 0.0
    if add_total:
        total_n = sum(denominators.values())
        counts_df["Total"] = [sum(int(pattern_counts.get(ds, Counter()).get(p, 0)) for ds in datasets) for p in PATTERN_ORDER]
        prev_df["Total"] = [round(100.0 * float(counts_df.loc[p, "Total"]) / total_n, 1) if total_n else 0.0 for p in PATTERN_ORDER]
    counts_df.loc["Papers (N)"] = [denominators.get(ds, 0) for ds in datasets] + ([sum(denominators.values())] if add_total else [])
    prev_df.loc["Papers (N)"] = [denominators.get(ds, 0) for ds in datasets] + ([sum(denominators.values())] if add_total else [])
    # Rename columns for readability in exported tables.
    counts_df = counts_df.rename(columns={ds: pretty_dataset_name(ds) for ds in datasets})
    prev_df = prev_df.rename(columns={ds: pretty_dataset_name(ds) for ds in datasets})
    return counts_df, prev_df


def df_to_latex(df: pd.DataFrame, caption: str, label: str) -> str:
    cols = list(df.columns)
    align = "l" + "r" * len(cols)
    lines = [
        r"\begin{table*}[ht]",
        r"\centering",
        rf"\caption{{{latex_escape(caption)}}}",
        rf"\label{{{latex_escape(label)}}}",
        rf"\begin{{tabular}}{{{align}}}",
        r"\toprule",
        r"\textbf{Pipeline pattern} & " + " & ".join(rf"\textbf{{{latex_escape(str(c))}}}" for c in cols) + r" \\",
        r"\midrule",
    ]
    for idx, row in df.iterrows():
        if idx == "Papers (N)":
            lines.append(r"\midrule")
        vals = []
        for c in cols:
            x = row[c]
            if idx == "Papers (N)":
                vals.append(str(int(float(x))))
            else:
                vals.append(f"{float(x):.1f}")
        lines.append(latex_escape(str(idx)) + " & " + " & ".join(vals) + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return "\n".join(lines)


def latex_escape(s: str) -> str:
    repl = {
        "\\": r"\textbackslash{}", "&": r"\&", "%": r"\%", "$": r"\$", "#": r"\#",
        "_": r"\_", "{": r"\{", "}": r"\}", "~": r"\textasciitilde{}", "^": r"\textasciicircum{}",
    }
    for k, v in repl.items():
        s = s.replace(k, v)
    return s


def df_to_pretty_text(df: pd.DataFrame) -> str:
    show = df.copy().astype(object)
    for idx in show.index:
        for col in show.columns:
            val = show.loc[idx, col]
            if idx == "Papers (N)":
                show.loc[idx, col] = str(int(float(val)))
            else:
                show.loc[idx, col] = f"{float(val):.1f}"
    return show.to_string()


def main() -> int:
    args = parse_args()
    regime = args.regime.upper()
    audit_path = Path(args.audit_results_dir) / regime / f"{regime}_paper_audit_log.csv"
    openalex_dir = Path(args.openalex_base_dir) / regime
    out_dir = Path(args.output_dir) / regime
    out_dir.mkdir(parents=True, exist_ok=True)

    if not audit_path.exists():
        print(f"ERROR: audit log not found: {audit_path}", file=sys.stderr)
        return 1

    audit = pd.read_csv(audit_path)
    flag = detect_accessible_flag(audit, args.accessible_flag)
    audit["_accessible"] = norm_bool_series(audit[flag])
    accessible = audit[audit["_accessible"]].copy()

    if "dataset" not in accessible.columns:
        raise ValueError("Audit log must contain a 'dataset' column.")
    accessible["dataset"] = accessible["dataset"].astype(str).str.strip().str.lower()

    if not args.quiet:
        print(f"Regime: {regime}")
        print(f"Audit log: {audit_path}")
        print(f"Accessible flag: {flag}")
        print(f"Accessible papers: {len(accessible)} / {len(audit)}")
        print(f"OpenAlex metadata dir: {openalex_dir}")

    oa_map = load_openalex_metadata(openalex_dir, regime)
    if not args.quiet:
        print(f"Loaded OpenAlex metadata rows indexed by title: {len(oa_map)}")

    # Prepare rows and metadata text.
    rows = []
    for i, row in accessible.iterrows():
        ds = str(row.get("dataset", "")).strip().lower()
        title = clean_text(row.get("title", ""))
        key = (ds, normalize_title_key(title))
        meta = oa_map.get(key, {})
        abstract = meta.get("abstract", "")
        doi = normalize_doi_url(row.get("doi_or_url", "") or row.get("doi", "") or meta.get("doi_or_url_oa", ""))
        if not title:
            title = meta.get("title_oa", "")
        base_text = clean_text(" ".join([title, abstract]))
        rows.append({
            "audit_index": i,
            "dataset": ds,
            "title": title,
            "abstract": abstract,
            "doi_or_url": doi,
            "base_text": base_text,
            "source_csv": meta.get("source_csv", row.get("source_csv", "")),
            "openalex_id": meta.get("openalex_id", ""),
            "year": row.get("year", meta.get("year_oa", "")),
        })

    fetch_results: Dict[int, FetchResult] = {}
    if args.fetch_fulltext:
        to_fetch = [(idx, r["doi_or_url"]) for idx, r in enumerate(rows) if r["doi_or_url"]]
        if not args.quiet:
            print(f"Trying full-text/DOI retrieval for {len(to_fetch)} papers...")
        if args.doi_workers <= 1:
            fetcher = FullTextFetcher(args.timeout, args.sleep_sec)
            iterator = to_fetch
            if tqdm is not None and not args.quiet:
                iterator = tqdm(to_fetch, desc=f"{regime} full text", unit="paper", dynamic_ncols=True)
            for idx, url in iterator:
                fetch_results[idx] = fetcher.fetch(url)
        else:
            # Each worker creates its own session via helper.
            def work(item: Tuple[int, str]) -> Tuple[int, FetchResult]:
                idx, url = item
                fetcher = FullTextFetcher(args.timeout, args.sleep_sec)
                return idx, fetcher.fetch(url)
            with cf.ThreadPoolExecutor(max_workers=args.doi_workers) as ex:
                iterator = ex.map(work, to_fetch)
                if tqdm is not None and not args.quiet:
                    iterator = tqdm(iterator, total=len(to_fetch), desc=f"{regime} full text", unit="paper", dynamic_ncols=True)
                for idx, res in iterator:
                    fetch_results[idx] = res

    pattern_counts: Dict[str, Counter] = defaultdict(Counter)
    denominators: Dict[str, int] = Counter()
    audit_rows = []
    source_counter = Counter()
    matched_any_counter = Counter()

    iterator = enumerate(rows)
    if tqdm is not None and not args.quiet:
        iterator = tqdm(list(iterator), desc=f"{regime} pattern parse", unit="paper", dynamic_ncols=True)

    for idx, r in iterator:
        ds = r["dataset"]
        full_res = fetch_results.get(idx, FetchResult("", "metadata_only", r.get("doi_or_url", ""), ""))
        combined_text = clean_text(r["base_text"] + " " + full_res.text)
        text_available = len(combined_text) >= args.min_text_chars
        # Denominator is accessible papers that have enough text to assess. Since accessible papers
        # are already taken from the prior audit, this usually equals accessible papers except for
        # malformed rows with no title/abstract at all.
        if text_available:
            denominators[ds] += 1
        patterns, evidence = detect_pipeline_patterns(combined_text, args.method_window) if text_available else ([], {})
        if text_available and patterns:
            matched_any_counter[ds] += 1
            for ptn in patterns:
                pattern_counts[ds][ptn] += 1
        source = full_res.source if full_res.text else "metadata_only"
        source_counter[source] += 1
        audit_rows.append({
            "dataset": ds,
            "title": r["title"],
            "doi_or_url": r["doi_or_url"],
            "source_csv": r["source_csv"],
            "openalex_id": r["openalex_id"],
            "year": r["year"],
            "accessible_flag": flag,
            "text_available": text_available,
            "text_chars": len(combined_text),
            "text_source": source,
            "fetch_error": full_res.error,
            "patterns_detected": "; ".join(patterns),
            "num_patterns": len(patterns),
            **{f"pattern__{name}": int(name in patterns) for name in PATTERN_ORDER},
            **{f"evidence__{name}": evidence.get(name, "") for name in PATTERN_ORDER},
        })

    counts_df, prev_df = counts_to_tables(pattern_counts, denominators, add_total=not args.no_total_column)

    # Coverage table: prior audit accessible vs assessable text vs any detected pattern.
    all_datasets = sorted(accessible["dataset"].unique().tolist())
    coverage_rows = []
    for ds in all_datasets:
        accessible_n = int((accessible["dataset"] == ds).sum())
        assessable_n = int(denominators.get(ds, 0))
        any_pattern_n = int(matched_any_counter.get(ds, 0))
        coverage_rows.append({
            "dataset": pretty_dataset_name(ds),
            "accessible_papers": accessible_n,
            "assessable_text_papers": assessable_n,
            "papers_with_at_least_one_pattern": any_pattern_n,
            "assessable_over_accessible_percent": round(100.0 * assessable_n / accessible_n, 1) if accessible_n else 0.0,
            "any_pattern_over_assessable_percent": round(100.0 * any_pattern_n / assessable_n, 1) if assessable_n else 0.0,
        })
    coverage_df = pd.DataFrame(coverage_rows)

    prefix = f"{regime}_pipeline_patterns_from_audit"
    prev_df.to_csv(out_dir / f"{prefix}_prevalence_by_dataset.csv")
    counts_df.to_csv(out_dir / f"{prefix}_counts_by_dataset.csv")
    coverage_df.to_csv(out_dir / f"{prefix}_coverage_by_dataset.csv", index=False)
    pd.DataFrame(audit_rows).to_csv(out_dir / f"{prefix}_audit_log.csv", index=False)
    pd.DataFrame([{"text_source": k, "count": v} for k, v in source_counter.items()]).to_csv(out_dir / f"{prefix}_text_source_report.csv", index=False)

    (out_dir / f"{prefix}_prevalence_by_dataset.txt").write_text(df_to_pretty_text(prev_df), encoding="utf-8")
    (out_dir / f"{prefix}_counts_by_dataset.txt").write_text(df_to_pretty_text(counts_df), encoding="utf-8")
    caption = (
        f"{regime} regime: prevalence of pipeline architecture patterns across datasets. "
    )
    (out_dir / f"{prefix}_prevalence_by_dataset.tex").write_text(
        df_to_latex(prev_df, caption, f"tab:{regime}_pipeline_patterns"), encoding="utf-8"
    )

    report = {
        "regime": regime,
        "audit_log": str(audit_path),
        "openalex_dir": str(openalex_dir),
        "accessible_flag": flag,
        "audit_rows_total": int(len(audit)),
        "accessible_papers": int(len(accessible)),
        "assessable_papers": int(sum(denominators.values())),
        "papers_with_at_least_one_pattern": int(sum(matched_any_counter.values())),
        "fetch_fulltext": bool(args.fetch_fulltext),
        "text_source_counts": dict(source_counter),
        "patterns": PATTERN_ORDER,
    }
    (out_dir / f"{prefix}_report.json").write_text(json.dumps(report, indent=2), encoding="utf-8")

    if not args.quiet:
        print("\nPrevalence table:")
        print(df_to_pretty_text(prev_df))
        print("\nCoverage:")
        print(coverage_df.to_string(index=False))
        print(f"\nWrote outputs to: {out_dir.resolve()}")
    else:
        print(f"Wrote outputs to: {out_dir.resolve()}")
    return 0

