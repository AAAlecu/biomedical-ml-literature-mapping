def norm_bool_series(s: pd.Series) -> pd.Series:
    if s.dtype == bool:
        return s.fillna(False)
    return s.astype(str).str.strip().str.lower().isin(["true", "1", "yes", "y", "t"])


def detect_accessible_flag(df: pd.DataFrame, requested: str = "") -> str:
    if requested:
        if requested not in df.columns:
            raise ValueError(f"Requested --accessible-flag '{requested}' not found in audit log columns.")
        return requested
    for c in ["include_any_use", "used_any", "include_primary_only", "used_primary"]:
        if c in df.columns:
            return c
    raise ValueError(
        "Could not detect accessible-paper flag. Expected one of: "
        "include_any_use, used_any, include_primary_only, used_primary. "
        "Pass --accessible-flag explicitly if your audit log uses another column."
    )


def slug_dataset_from_filename(path: Path, regime: str) -> str:
    name = path.stem.lower()
    name = re.sub(r"^openalex_", "", name)
    name = re.sub(rf"_{regime.lower()}_ml_top\d+$", "", name)
    return name


def choose_column(df: pd.DataFrame, candidates: Sequence[str]) -> Optional[str]:
    lower = {str(c).lower(): c for c in df.columns}
    for cand in candidates:
        if cand.lower() in lower:
            return lower[cand.lower()]
    return None


def normalize_doi_url(value: str) -> str:
    value = str(value or "").strip()
    if not value or value.lower() in {"nan", "none", "na"}:
        return ""
    # Keep first DOI/URL if multiple values were serialized.
    parts = [p.strip() for p in re.split(r"[;\n\t]+", value) if p.strip()]
    value = parts[0] if parts else value
    if value.startswith("10."):
        return "https://doi.org/" + value
    return value


def normalize_title_key(title: str) -> str:
    title = str(title or "").lower()
    title = re.sub(r"[^a-z0-9]+", " ", title)
    return re.sub(r"\s+", " ", title).strip()


def clean_text(text: str) -> str:
    text = str(text or "")
    text = re.sub(r"<[^>]+>", " ", text)
    text = text.replace("&amp;", "&")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def load_openalex_metadata(openalex_dir: Path, regime: str) -> Dict[Tuple[str, str], Dict[str, str]]:
    """Return mapping (dataset, normalized_title) -> metadata dict."""
    mapping: Dict[Tuple[str, str], Dict[str, str]] = {}
    for path in sorted(openalex_dir.glob(f"openalex_*_{regime}_ml_top*.csv")):
        dataset = slug_dataset_from_filename(path, regime)
        try:
            df = pd.read_csv(path)
        except Exception:
            continue
        title_col = choose_column(df, ["title", "display_name"])
        abstract_col = choose_column(df, ["abstract"])
        doi_col = choose_column(df, ["doi", "DOI", "doi_url", "url", "link", "links"])
        openalex_col = choose_column(df, ["openalex_id", "id"])
        year_col = choose_column(df, ["year", "publication_year"])
        if title_col is None:
            continue
        for _, row in df.iterrows():
            title = clean_text(row.get(title_col, ""))
            key = (dataset, normalize_title_key(title))
            if not key[1]:
                continue
            mapping[key] = {
                "title_oa": title,
                "abstract": clean_text(row.get(abstract_col, "")) if abstract_col else "",
                "doi_or_url_oa": normalize_doi_url(row.get(doi_col, "")) if doi_col else "",
                "openalex_id": clean_text(row.get(openalex_col, "")) if openalex_col else "",
                "year_oa": clean_text(row.get(year_col, "")) if year_col else "",
                "source_csv": path.name,
            }
    return mapping


def extract_method_like_text(text: str, window: int) -> str:
    if window <= 0:
        return text
    lower = text.lower()
    headings = [
        "methods", "materials and methods", "methodology", "model", "models",
        "model architecture", "architecture", "approach", "framework", "pipeline",
        "experiments", "implementation", "statistical analysis", "machine learning",
    ]
    chunks = []
    for h in headings:
        for m in re.finditer(re.escape(h), lower):
            start = max(0, m.start() - window // 3)
            end = min(len(text), m.end() + window)
            chunks.append(text[start:end])
            if len(chunks) >= 12:
                break
        if len(chunks) >= 12:
            break
    if not chunks:
        return text
    # Include title/abstract beginning plus method-like chunks.
    return (text[:4000] + " " + " ".join(chunks))[:100_000]

def detect_pipeline_patterns(text, method_window=8000):
    target = extract_method_like_text(text, method_window)
    found = []
    evidence = {}

    for name, pats in PIPELINE_PATTERNS:

        for pat in pats:
            m = pat.search(target)
            if m:
                found.append(name)
                evidence[name] = re.sub(
                    r"\s+",
                    " ",
                    target[max(0, m.start()-120):min(len(target), m.end()+180)]
                ).strip()
                break

    return found, evidence

@dataclass
class FetchResult:
    text: str
    source: str
    url: str
    error: str = ""


class FullTextFetcher:
    def __init__(self, timeout: int, sleep_sec: float):
        self.timeout = timeout
        self.sleep_sec = sleep_sec
        self.session = None
        if requests is not None:
            self.session = requests.Session()
            self.session.headers.update({"User-Agent": USER_AGENT})

    def fetch(self, url: str) -> FetchResult:
        url = normalize_doi_url(url)
        if not url or not re.match(r"https?://", url, flags=re.I):
            return FetchResult("", "metadata_only", url, "no_url")
        if self.session is None:
            return FetchResult("", "metadata_only", url, "requests_unavailable")
        try:
            time.sleep(self.sleep_sec)
            r = self.session.get(url, timeout=self.timeout, allow_redirects=True)
            r.raise_for_status()
        except Exception as e:
            return FetchResult("", "metadata_only", url, f"request_failed:{type(e).__name__}")

        ctype = (r.headers.get("Content-Type") or "").lower()
        final_url = r.url
        if "pdf" in ctype or final_url.lower().split("?")[0].endswith(".pdf"):
            txt = extract_pdf_text(r.content)
            return FetchResult(txt, "pdf", final_url, "" if txt else "pdf_no_text")

        html = r.text or ""
        html_text = extract_html_text(html)
        pdf_url = find_pdf_url(final_url, html)
        if pdf_url:
            try:
                time.sleep(self.sleep_sec)
                pr = self.session.get(pdf_url, timeout=self.timeout, allow_redirects=True)
                if pr.ok and ("pdf" in (pr.headers.get("Content-Type") or "").lower() or pr.url.lower().split("?")[0].endswith(".pdf")):
                    pdf_text = extract_pdf_text(pr.content)
                    if pdf_text:
                        combined = clean_text(html_text + " " + pdf_text)
                        return FetchResult(combined, "html_plus_pdf", pr.url, "")
            except Exception:
                pass
        return FetchResult(html_text, "html", final_url, "" if html_text else "html_no_text")


def extract_html_text(html: str) -> str:
    if not html or BeautifulSoup is None:
        return ""
    try:
        soup = BeautifulSoup(html, "html.parser")
        for tag in soup(["script", "style", "noscript", "svg"]):
            tag.decompose()
        pieces = []
        for meta_name in ["citation_title", "description", "citation_abstract", "dc.description"]:
            tag = soup.find("meta", attrs={"name": meta_name})
            if tag and tag.get("content"):
                pieces.append(tag["content"])
        for meta_prop in ["og:title", "og:description"]:
            tag = soup.find("meta", attrs={"property": meta_prop})
            if tag and tag.get("content"):
                pieces.append(tag["content"])
        for tag in soup.find_all(["title", "h1", "h2", "h3", "p", "section", "article", "li"]):
            txt = tag.get_text(" ", strip=True)
            if txt and len(txt) > 20:
                pieces.append(txt)
        return clean_text(" ".join(pieces))[:HTML_TEXT_LIMIT]
    except Exception:
        return ""


def find_pdf_url(base_url: str, html: str) -> str:
    if not html or BeautifulSoup is None:
        return ""
    try:
        soup = BeautifulSoup(html, "html.parser")
        candidates = []
        for tag in soup.find_all("meta"):
            if tag.get("name", "").lower() in {"citation_pdf_url", "dc.identifier"} and tag.get("content"):
                val = tag.get("content")
                if ".pdf" in val.lower():
                    candidates.append(urljoin(base_url, val))
        for a in soup.find_all("a", href=True):
            href = a.get("href") or ""
            label = a.get_text(" ", strip=True).lower()
            if ".pdf" in href.lower() or "pdf" in label or "download" in label:
                candidates.append(urljoin(base_url, href))
        seen = set()
        for c in candidates:
            if c not in seen:
                seen.add(c)
                return c
    except Exception:
        return ""
    return ""


def extract_pdf_text(data: bytes) -> str:
    if not data or PdfReader is None:
        return ""
    try:
        reader = PdfReader(io.BytesIO(data), strict=False)
        pages = []
        for page in list(reader.pages)[:PDF_PAGE_LIMIT]:
            try:
                txt = page.extract_text() or ""
            except Exception:
                txt = ""
            if txt:
                pages.append(txt)
        return clean_text(" ".join(pages))[:HTML_TEXT_LIMIT]
    except Exception:
        return ""


