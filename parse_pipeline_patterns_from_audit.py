def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Parse pipeline architecture patterns from paper audit logs.")
    p.add_argument("--regime", default="H", choices=["S", "H", "M", "T"], help="Regime to process.")
    p.add_argument("--audit-results-dir", default="./regime_parse_outputs", help="Directory containing <REGIME>/<REGIME>_paper_audit_log.csv.")
    p.add_argument("--openalex-base-dir", default="./query_outputs", help="Directory containing <REGIME>/openalex_*_<REGIME>_ml_top350.csv.")
    p.add_argument("--output-dir", default="./pipeline_parse_output", help="Output directory.")
    p.add_argument("--accessible-flag", default="", help="Audit-log boolean column defining accessible papers. If omitted, auto-detected.")
    p.add_argument("--fetch-fulltext", action="store_true", default=True, help="Try DOI/URL landing pages and PDFs after title/abstract.")
    p.add_argument("--doi-workers", type=int, default=1, help="Parallel workers for DOI/full-text retrieval. Used only with --fetch-fulltext.")
    p.add_argument("--timeout", type=int, default=25, help="HTTP timeout in seconds.")
    p.add_argument("--sleep-sec", type=float, default=0.25, help="Polite sleep between requests per worker.")
    p.add_argument("--min-text-chars", type=int, default=80, help="Minimum combined metadata text chars to keep a paper assessable.")
    p.add_argument("--method-window", type=int, default=8000, help="Chars around method-like headings prioritized in matching; 0 disables section focus.")
    p.add_argument("--no-total-column", action="store_true", help="Do not add Total column to prevalence/count tables.")
    p.add_argument("--quiet", action="store_true", help="Reduce console output.")
    return p.parse_args()

