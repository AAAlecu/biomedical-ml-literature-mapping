#!/usr/bin/env python3
"""
Dataset-Regime ML Model Parsing Script
--------------------------------------

Purpose
-------
Parses previously retrieved OpenAlex dataset-regime CSV files and performs
large-scale rule-based extraction of machine learning models from titles,
abstracts, and optionally DOI landing pages/full-text content.

The script is designed to support the four prediction regimes:

    S = structured multiscale prediction
    H = high-dimensional biomedical signals
    M = multimodal learning
    T = temporal and longitudinal modeling

The script:
1) loads one or more OpenAlex CSV exports generated by the dataset query pipeline
2) filters papers using regime-specific regex pattern libraries
3) extracts exact machine learning model names using extensive regex matching
4) maps exact models into broader model-family categories
5) optionally retrieves additional text from DOI landing pages or PDFs
6) identifies:
      - model occurances (labeled here "any-use" models)
      - dominant model occurances (labeled here "primary-only" models)
7) builds:
      - dataset-level model-family frequency tables
      - exact-model frequency tables
      - any-use and primary-only statistics
8) exports:
      - CSV summary tables
      - formatted TXT tables
      - formatted LaTeX tables
      - audit logs for every parsed paper
      - per-dataset count CSV files
9) generates model-family → exact-model mapping tables

"""

