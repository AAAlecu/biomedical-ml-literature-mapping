def slugify_dataset(path: str) -> str:
    name = Path(path).stem.lower()
    name = re.sub(r"^openalex_", "", name)
    name = re.sub(r"_[shmt]_ml_top\d+$", "", name)
    name = re.sub(r"[^a-z0-9]+", "_", name).strip("_")
    return name


def ensure_dir(path: str | Path) -> str:
    path = str(Path(path).resolve())
    Path(path).mkdir(parents=True, exist_ok=True)
    return path


def choose_column(df: pd.DataFrame, candidates: Iterable[str]) -> Optional[str]:
    lower = {c.lower(): c for c in df.columns}
    for cand in candidates:
        if cand.lower() in lower:
            return lower[cand.lower()]
    return None


def get_text_fields(row: pd.Series, title_col: str, abstract_col: Optional[str], goal_col: Optional[str]) -> Tuple[str, str, str]:
    title = str(row.get(title_col, "") or "")
    abstract = str(row.get(abstract_col, "") or "") if abstract_col else ""
    goal = str(row.get(goal_col, "") or "") if goal_col else ""
    return title, abstract, goal


def normalize_text(text: str) -> str:
    text = re.sub(r"\s+", " ", text or " ")
    return text.strip()


def regime_score(text: str, regime: str) -> int:
    pats = REGIME_PATTERNS.get(regime, [])
    return sum(1 for p in pats if re.search(p, text, flags=re.I))


def detect_models(text: str) -> Tuple[List[str], Dict[str, str]]:
    exact: List[str] = []
    mapping: Dict[str, str] = {}
    for model, family, patterns in MODEL_PATTERNS:
        if any(re.search(p, text, flags=re.I) for p in patterns):
            exact.append(model)
            mapping[model] = family
    return exact, mapping


def choose_primary_model(text: str, exact_models: List[str], model_to_family: Dict[str, str]) -> Tuple[Optional[str], Optional[str], str]:
    if not exact_models:
        return None, None, "no_identifiable_model"

    primary_cues = [
        r"we (?:developed|proposed|used|trained|applied|implemented|constructed) (?:an?|the)?\s*{name}",
        r"{name} (?:model|classifier|regressor|framework|approach)",
        r"using (?:an?|the)?\s*{name}",
        r"based on (?:an?|the)?\s*{name}",
    ]

    scores: Dict[str, int] = {m: 0 for m in exact_models}
    for m in exact_models:
        base = re.escape(m)
        for cue in primary_cues:
            if re.search(cue.format(name=base), text, flags=re.I):
                scores[m] += 2
        if re.search(base, text, flags=re.I):
            scores[m] += 1

    if not scores:
        return None, None, "primary_unclear"

    best = max(scores.values())
    winners = [m for m, s in scores.items() if s == best and s > 0]
    if len(winners) != 1:
        return None, None, "primary_unclear"

    m = winners[0]
    return m, model_to_family[m], "ok"


def fetch_url_text(url: str, timeout: int = 25) -> str:
    try:
        r = requests.get(url, headers=HEADERS, timeout=timeout, allow_redirects=True)
        r.raise_for_status()
        ctype = (r.headers.get("Content-Type") or "").lower()
        if "pdf" in ctype or url.lower().endswith(".pdf"):
            return extract_pdf_text_bytes(r.content)
        soup = BeautifulSoup(r.text, "html.parser")
        for tag in soup(["script", "style", "noscript"]):
            tag.decompose()
        return normalize_text(soup.get_text(" ", strip=True))
    except Exception:
        return ""


def extract_pdf_text_bytes(data: bytes) -> str:
    if PyPDF2 is None:
        return ""
    try:
        reader = PyPDF2.PdfReader(io.BytesIO(data), strict=False)
        pages = []
        for page in reader.pages[:25]:
            try:
                pages.append(page.extract_text() or "")
            except Exception:
                continue
        return normalize_text(" ".join(pages))
    except Exception:
        return ""


def maybe_fetch_extra_text(doi_or_url: str, disable_fetch: bool, timeout: int, sleep_sec: float) -> str:
    if disable_fetch:
        return ""
    value = (doi_or_url or "").strip()
    if not value:
        return ""
    candidates = [u.strip() for u in re.split(r"[;,\s]+", value) if u.strip()]
    for cand in candidates:
        if cand.startswith("10."):
            cand = f"https://doi.org/{cand}"
        elif not re.match(r"https?://", cand, flags=re.I) and "/" in cand and cand.startswith("10."):
            cand = f"https://doi.org/{cand}"
        elif not re.match(r"https?://", cand, flags=re.I):
            continue
        text = fetch_url_text(cand, timeout=timeout)
        time.sleep(sleep_sec)
        if text:
            return text
    return ""


def is_relevant_to_regime(text: str, regime: str, exact_models: List[str], min_score: int = 1) -> bool:
    score = regime_score(text, regime)
    if regime == "S":
        # Permissive logic for S: if there is at least one structured cue, or at least one model and no stronger H/M/T cues.
        if score >= 1:
            return True
        other_scores = {r: regime_score(text, r) for r in ["H", "M", "T"]}
        if exact_models and max(other_scores.values(), default=0) == 0:
            return True
        return False
    return score >= min_score


