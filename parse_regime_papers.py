def main() -> int:
    args = parse_args()
    input_dir = Path(args.input_dir)
    out_dir = ensure_dir(args.output_dir)
    csv_paths = sorted(glob.glob(str(input_dir / args.input_glob)))
    if not csv_paths:
        print(f"No files matched: {input_dir / args.input_glob}", file=sys.stderr)
        return 1

    dataset_any_model_counts: Dict[str, Counter] = defaultdict(Counter)
    dataset_any_family_counts: Dict[str, Counter] = defaultdict(Counter)
    dataset_primary_model_counts: Dict[str, Counter] = defaultdict(Counter)
    dataset_primary_family_counts: Dict[str, Counter] = defaultdict(Counter)
    dataset_any_n: Dict[str, int] = defaultdict(int)
    dataset_primary_n: Dict[str, int] = defaultdict(int)
    family_to_models: Dict[str, Set[str]] = defaultdict(set)
    audit_rows: List[dict] = []
    summary_rows: List[dict] = []

    for csv_path in csv_paths:
        dataset = slugify_dataset(csv_path)
        df = pd.read_csv(csv_path)
        title_col = choose_column(df, ["title"])
        abstract_col = choose_column(df, ["abstract"])
        goal_col = choose_column(df, ["Goal", "goal"])
        doi_col = choose_column(df, ["DOI", "doi", "doi_url", "link", "url", "links"])
        if title_col is None:
            print(f"Skipping {csv_path}: no title column.", file=sys.stderr)
            continue

        total = len(df)
        used_any = 0
        used_primary = 0

        iterator = df.iterrows()
        if tqdm is not None:
            iterator = tqdm(iterator, total=total, desc=f"{dataset}", disable=False, leave=True)

        for idx, row in iterator:
            title, abstract, goal = get_text_fields(row, title_col, abstract_col, goal_col)
            base_text = normalize_text(" ".join([title, abstract, goal]))
            doi_or_url = str(row.get(doi_col, "") or "") if doi_col else ""
            extra_text = maybe_fetch_extra_text(doi_or_url, args.disable_doi_fetch, args.timeout, args.sleep_sec)
            full_text = normalize_text(" ".join([base_text, extra_text]))
            exact_models, model_to_family = detect_models(full_text)
            relevant = is_relevant_to_regime(full_text, args.regime, exact_models, min_score=args.min_regime_score)

            include_any = bool(relevant and exact_models)
            primary_model, primary_family, primary_reason = choose_primary_model(full_text, exact_models, model_to_family) if include_any else (None, None, "not_included_any")
            include_primary = bool(primary_model)

            if include_any:
                used_any += 1
                dataset_any_n[dataset] += 1
                for model in sorted(set(exact_models)):
                    dataset_any_model_counts[dataset][model] += 1
                    fam = model_to_family[model]
                    dataset_any_family_counts[dataset][fam] += 1
                    family_to_models[fam].add(model)

            if include_primary:
                used_primary += 1
                dataset_primary_n[dataset] += 1
                dataset_primary_model_counts[dataset][primary_model] += 1
                dataset_primary_family_counts[dataset][primary_family] += 1

            audit_rows.append(
                {
                    "dataset": dataset,
                    "source_csv": os.path.basename(csv_path),
                    "row_index": idx,
                    "title": title,
                    "doi_or_url": doi_or_url,
                    "regime": args.regime,
                    "regime_score": regime_score(full_text, args.regime),
                    "relevant": relevant,
                    "models_detected": "; ".join(exact_models),
                    "families_detected": "; ".join(sorted({model_to_family[m] for m in exact_models})),
                    "include_any_use": include_any,
                    "primary_model": primary_model or "",
                    "primary_family": primary_family or "",
                    "include_primary_only": include_primary,
                    "primary_reason": primary_reason,
                }
            )

            if tqdm is not None and hasattr(iterator, "set_postfix_str"):
                iterator.set_postfix_str(f"parsed={used_any}/{total}")

        summary_rows.append({"dataset": dataset, "provided": total, "used_any": used_any, "used_primary": used_primary})

    # Main family percentage tables
    any_df = counts_to_percentage_table(dataset_any_family_counts, dataset_any_n, FAMILY_ORDER)
    primary_df = counts_to_percentage_table(dataset_primary_family_counts, dataset_primary_n, FAMILY_ORDER)
    any_model_df = counts_to_percentage_table(dataset_any_model_counts, dataset_any_n)
    primary_model_df = counts_to_percentage_table(dataset_primary_model_counts, dataset_primary_n)

    # Write outputs
    pd.DataFrame(audit_rows).to_csv(Path(out_dir) / f"{args.regime}_paper_audit_log.csv", index=False)
    pd.DataFrame(summary_rows).to_csv(Path(out_dir) / f"{args.regime}_summary_counts.csv", index=False)

    any_df.to_csv(Path(out_dir) / f"{args.regime}_any_use_table.csv")
    primary_df.to_csv(Path(out_dir) / f"{args.regime}_primary_only_table.csv")
    any_model_df.to_csv(Path(out_dir) / f"{args.regime}_any_use_model_table.csv")
    primary_model_df.to_csv(Path(out_dir) / f"{args.regime}_primary_only_model_table.csv")

    (Path(out_dir) / f"{args.regime}_any_use_table.txt").write_text(df_to_pretty_text(any_df), encoding="utf-8")
    (Path(out_dir) / f"{args.regime}_primary_only_table.txt").write_text(df_to_pretty_text(primary_df), encoding="utf-8")
    (Path(out_dir) / f"{args.regime}_any_use_model_table.txt").write_text(df_to_pretty_text(any_model_df), encoding="utf-8")
    (Path(out_dir) / f"{args.regime}_primary_only_model_table.txt").write_text(df_to_pretty_text(primary_model_df), encoding="utf-8")

    (Path(out_dir) / f"{args.regime}_any_use_table.tex").write_text(
        df_to_mdpi_latex(any_df, f"{args.regime} regime: any-use model-family frequencies across datasets.", f"tab:{args.regime}_any_use"),
        encoding="utf-8",
    )
    (Path(out_dir) / f"{args.regime}_primary_only_table.tex").write_text(
        df_to_mdpi_latex(primary_df, f"{args.regime} regime: primary-only model-family frequencies across datasets.", f"tab:{args.regime}_primary_only"),
        encoding="utf-8",
    )
    (Path(out_dir) / f"{args.regime}_family_model_map.tex").write_text(
        family_model_map_latex(family_to_models, args.regime),
        encoding="utf-8",
    )

    report = {
        "regime": args.regime,
        "input_dir": str(input_dir.resolve()),
        "input_glob": args.input_glob,
        "output_dir": str(Path(out_dir).resolve()),
        "files_processed": len(csv_paths),
        "summary": summary_rows,
    }
    (Path(out_dir) / f"{args.regime}_report.txt").write_text(json.dumps(report, indent=2), encoding="utf-8")

    if args.extra_dataset_count_csvs:
        written = write_extra_dataset_count_csvs(
            out_dir,
            args.regime,
            dataset_any_model_counts,
            dataset_any_family_counts,
            dataset_primary_model_counts,
            dataset_primary_family_counts,
            dataset_any_n,
            dataset_primary_n,
        )
        print(f"[extra] Target directory: {Path(out_dir).resolve() / (args.regime + '_extra_dataset_counts')}", file=sys.stderr)
        print(f"[extra] Files written: {written}", file=sys.stderr)

    summary_df = pd.DataFrame(summary_rows)
    if not summary_df.empty:
        print(summary_df.to_string(index=False))
    print(f"Output directory: {Path(out_dir).resolve()}")
    return 0


