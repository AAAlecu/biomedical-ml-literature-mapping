def counts_to_percentage_table(dataset_to_counter: Dict[str, Counter], dataset_to_n: Dict[str, int], row_order: Optional[List[str]] = None) -> pd.DataFrame:
    datasets = sorted(dataset_to_counter)
    rows: Set[str] = set()
    for c in dataset_to_counter.values():
        rows.update(c.keys())
    ordered_rows = [r for r in (row_order or []) if r in rows] + sorted(r for r in rows if r not in set(row_order or []))
    data = {}
    total_counter = Counter()
    total_n = 0
    for ds in datasets:
        n = dataset_to_n.get(ds, 0)
        col = []
        for row in ordered_rows:
            val = dataset_to_counter[ds].get(row, 0)
            col.append(round((100.0 * val / n), 1) if n else 0.0)
            total_counter[row] += val
        data[ds] = col
        total_n += n
    total_vals = [round((100.0 * total_counter[row] / total_n), 1) if total_n else 0.0 for row in ordered_rows]
    df = pd.DataFrame(data, index=ordered_rows)
    df["Total"] = total_vals
    df.loc["Papers (N)"] = [dataset_to_n.get(ds, 0) for ds in datasets] + [total_n]
    return df


def counts_to_count_table(counter: Counter, n: int) -> pd.DataFrame:
    items = sorted(counter.items(), key=lambda x: (-x[1], x[0].lower()))
    df = pd.DataFrame({"Count": [v for _, v in items]}, index=[k for k, _ in items])
    df.loc["Papers (N)"] = n
    return df


def df_to_pretty_text(df: pd.DataFrame) -> str:
    show = df.copy().astype(object)
    for idx in show.index:
        for col in show.columns:
            val = show.loc[idx, col]
            try:
                if idx == "Papers (N)":
                    show.loc[idx, col] = str(int(float(val)))
                else:
                    show.loc[idx, col] = f"{float(val):.1f}"
            except Exception:
                show.loc[idx, col] = str(val)
    return show.to_string()


def latex_escape(s: str) -> str:
    repl = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
    }
    for k, v in repl.items():
        s = s.replace(k, v)
    return s


def df_to_mdpi_latex(df: pd.DataFrame, caption: str, label: str) -> str:
    cols = df.columns.tolist()
    align = "l" + "r" * len(cols)
    lines = [
        r"\begin{table*}[ht]",
        r"\centering",
        rf"\caption{{{latex_escape(caption)}}}",
        rf"\label{{{latex_escape(label)}}}",
        rf"\begin{{tabular}}{{{align}}}",
        r"\toprule",
        "Model family" + " & " + " & ".join(latex_escape(str(c)) for c in cols) + r" \\",
        r"\midrule",
    ]
    for idx in df.index:
        vals = []
        for col in cols:
            x = df.loc[idx, col]
            if idx == "Papers (N)":
                vals.append(str(int(float(x))))
            else:
                vals.append(f"{float(x):.1f}")
        if idx == "Papers (N)":
            lines.append(r"\midrule")
        lines.append(latex_escape(str(idx)) + " & " + " & ".join(vals) + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return "\n".join(lines)


def family_model_map_latex(family_to_models: Dict[str, Set[str]], regime: str) -> str:
    lines = [
        r"\begin{table}[ht]",
        r"\centering",
        rf"\caption{{Model families and exact models identified in the {latex_escape(regime)} regime.}}",
        rf"\label{{tab:{latex_escape(regime)}_model_families}}",
        r"\begin{tabular}{p{4.2cm} p{10.5cm}}",
        r"\toprule",
        r"\textbf{Model family} & \textbf{Exact models identified} \\",
        r"\midrule",
    ]
    for family in FAMILY_ORDER:
        models = sorted(family_to_models.get(family, set()))
        if not models:
            continue
        lines.append(latex_escape(family) + " & " + latex_escape("; ".join(models)) + r" \\")
        lines.append("")
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table}"])
    return "\n".join(lines)


