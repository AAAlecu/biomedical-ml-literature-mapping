def write_extra_dataset_count_csvs(
    out_dir: str,
    regime: str,
    dataset_any_model_counts: Dict[str, Counter],
    dataset_any_family_counts: Dict[str, Counter],
    dataset_primary_model_counts: Dict[str, Counter],
    dataset_primary_family_counts: Dict[str, Counter],
    dataset_any_n: Dict[str, int],
    dataset_primary_n: Dict[str, int],
) -> int:
    extra_dir = ensure_dir(Path(out_dir) / f"{regime}_extra_dataset_counts")
    manifest_rows = []
    written = 0

    all_datasets = sorted(set(dataset_any_model_counts) | set(dataset_any_family_counts) | set(dataset_primary_model_counts) | set(dataset_primary_family_counts))
    for ds in all_datasets:
        bundle = [
            (f"{ds}_any_use_model_counts.csv", counts_to_count_table(dataset_any_model_counts.get(ds, Counter()), dataset_any_n.get(ds, 0))),
            (f"{ds}_any_use_family_counts.csv", counts_to_count_table(dataset_any_family_counts.get(ds, Counter()), dataset_any_n.get(ds, 0))),
            (f"{ds}_primary_only_model_counts.csv", counts_to_count_table(dataset_primary_model_counts.get(ds, Counter()), dataset_primary_n.get(ds, 0))),
            (f"{ds}_primary_only_family_counts.csv", counts_to_count_table(dataset_primary_family_counts.get(ds, Counter()), dataset_primary_n.get(ds, 0))),
        ]
        for fname, df in bundle:
            fpath = Path(extra_dir) / fname
            df.to_csv(fpath)
            exists = fpath.exists()
            written += int(exists)
            manifest_rows.append({"dataset": ds, "file": fname, "path": str(fpath.resolve()), "exists": exists})

    pd.DataFrame(manifest_rows).to_csv(Path(extra_dir) / "manifest.csv", index=False)
    return written


